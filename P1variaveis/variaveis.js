// var é variavel global, acessivel em qualquer parte do código

var nome = 'Flávio'

// let é a variavel que só pode acessivel dentro do bloco de código que esta declarada

let idade = 32

// para alterar uma variavel

idade = 45

// const é uma variavel que também só pode ser acessada dentro do mesmo bloco de código porém não pode ser alterada (por isso é uma constante)

const solteiro = true

console.log(idade) // vai mostrar no console 45 pois a variavel foi alterada
console.log(nome)
console.log(solteiro)
